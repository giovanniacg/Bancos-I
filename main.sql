-- Create Tables

CREATE TABLE Hospede (
  cpf TEXT,
  nome TEXT,
  idade INTEGER,
  PRIMARY KEY (cpf)
);

CREATE TABLE Cargo (
  nome TEXT PRIMARY KEY,
  descricao TEXT
);

CREATE TABLE Funcionario (
  cpf TEXT,
  nome TEXT,
  cargo TEXT,
  salario INTEGER,
  cpfgerente TEXT,
  PRIMARY KEY (cpf),
  FOREIGN KEY (cpfgerente) REFERENCES Funcionario (cpf)
  	ON DELETE SET NULL
  	ON UPDATE CASCADE
);

CREATE TABLE Contrato (
    url_pdf TEXT PRIMARY KEY,
    cpf_funcionario TEXT,
    data_inicio TEXT,
    data_fim TEXT,
    FOREIGN KEY (cpf_funcionario) REFERENCES Funcionario (cpf)
        ON DELETE CASCADE
        ON UPDATE CASCADE
);

CREATE TABLE Categoria (
  codigo INTEGER PRIMARY KEY AUTOINCREMENT,
  nome TEXT,
  descricao TEXT
);

CREATE TABLE Quarto (
  numero INTEGER,
  capacidade INTEGER,
  custo INTEGER,
  codigo_categoria INTEGER,
  PRIMARY KEY (numero),
  FOREIGN KEY (codigo_categoria) REFERENCES Categoria (codigo)
  	ON DELETE SET NULL
  	ON UPDATE CASCADE
);

CREATE TABLE Reserva (
  id INTEGER PRIMARY key AUTOINCREMENT,
  cpf_funcionario TEXT,
  data_checkin TEXT,
  data_checkout TEXT,
  FOREIGN KEY (cpf_funcionario) REFERENCES Funcionario (cpf)
  	ON DELETE SET NULL
  	ON UPDATE CASCADE
);

create TABLE ReservaQuarto (
  id_reserva INTEGER,
  numero_quarto INTEGER,
  PRIMARY KEY (id_reserva, numero_quarto),
  FOREIGN KEY (id_reserva) REFERENCES Reserva (id)
  	ON DELETE CASCADE
  	ON UPDATE CASCADE,
  FOREIGN KEY (numero_quarto) REFERENCES Quarto (numero)
  	ON DELETE CASCADE
  	ON UPDATE CASCADE
);

create TABLE ReservaHospede (
  id_reserva INTEGER,
  cpf_hospede TEXT,
  PRIMARY KEY (id_reserva, cpf_hospede),
  FOREIGN KEY (id_reserva) REFERENCES Reserva (id)
  	ON DELETE CASCADE
  	ON UPDATE CASCADE,
  FOREIGN KEY (cpf_hospede) REFERENCES Hospede (cpf)
  	ON DELETE CASCADE
  	ON UPDATE CASCADE
);

CREATE TABLE Servico (
  id INTEGER PRIMARY key AUTOINCREMENT,
  nome TEXT,
  custo_atual INTEGER
);

CREATE TABLE HospedeServico (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  cpf_hospede TEXT,
  id_servico INTEGER,
  data_aquisicao TEXT,
  custo_pago INTEGER,
  FOREIGN KEY (cpf_hospede) REFERENCES Hospede (cpf)
  	ON DELETE CASCADE
  	ON UPDATE CASCADE,
  FOREIGN KEY (id_servico) REFERENCES Servico (id)
  	on DELETE CASCADE
  	on UPDATE CASCADE
);

CREATE TABLE Cupom (
  cpf_hospede TEXT,
  id_servico INTEGER,
  valor_desconto INTEGER,
  data_validade TEXT,
  data_utilizado TEXT,
  PRIMARY KEY (cpf_hospede, id_servico),
  FOREIGN KEY (cpf_hospede) REFERENCES Hospede (cpf)
  	ON DELETE CASCADE
  	ON UPDATE CASCADE,
  FOREIGN KEY (id_servico) REFERENCES Servico (id)
  	on DELETE CASCADE
  	ON UPDATE CASCADE
);
  

